using AIHUB_Server.Domain.Entities.Users;

namespace AIHUB_Server.Application.Users.Commands.Authenticate
{
    public class UsersAuthenticateCommandResult
    {
        public int Id { get; set; }
        public string FirstName { get; set; }
        public string LastName { get; set; }
        public string Username { get; set; }
        public string Token { get; set; }

        public UsersAuthenticateCommandResult(User user, string token)
        {
            Id = user.Id;
            FirstName = user.FirstName;
            LastName = user.LastName;
            Username = user.Username;
            Token = token;
        }
    }
}
